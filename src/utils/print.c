#include "philo.h"

void	update_status(char *msg, t_philo *philo)
{
	printf(" %-6lu ms | N°: %-3d | %s\n",
		get_time() - philo->data->time->start, philo->id, msg);
}

void	prompt(void)
{
	printf("-----------------------------------------\n"
			"|   TIME   |    N°   |      ACTION      |\n"
			"-----------------------------------------\n");
}
